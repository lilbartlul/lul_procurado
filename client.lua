-----------------------------------------------------------------------------------------------------------------------------------------
-- importa os Utils do VRP
-----------------------------------------------------------------------------------------------------------------------------------------	
local Tunnel = module("vrp", "lib/Tunnel")
local Proxy = module("vrp", "lib/Proxy")
-----------------------------------------------------------------------------------------------------------------------------------------
-- Tunnel e Proxy VRP
-----------------------------------------------------------------------------------------------------------------------------------------
vRPclient = Tunnel.getInterface("vRP")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- Tunnel e Proxy do Resource
-----------------------------------------------------------------------------------------------------------------------------------------
vRPidd = {}
Tunnel.bindInterface("lul_procurado",vRPidd)
src = Tunnel.getInterface("lul_procurado")
-----------------------------------------------------------------------------------------------------------------------------------------
-- Recebendo tempo
-----------------------------------------------------------------------------------------------------------------------------------------
local tempo = 0
local pessoa = 0

RegisterNetEvent('passandoTabela')
AddEventHandler('passandoTabela', function(str)
    for k, v in pairs(str) do
        tempo = v
    end
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- Loop enviar para js caso esteja procurado
-----------------------------------------------------------------------------------------------------------------------------------------
Citizen.CreateThread(function()
    while true do 
        Citizen.Wait(5)
        src.enviandoTabelaJS()
        if tempo >= 1 then
            SendNUIMessage({
                type = "ui",
                tempo = tempo
            })
        else
            SendNUIMessage({
                type = "reset"
            })
        end
    end
end)